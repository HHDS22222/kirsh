package com.kirsh.www.biz.product;

public class ProductOptionVO {
	private String productSize;
	private int productInvntCnt;
	private String productModel;
	private int productOptionNum;
	private int productCode;
	
	public String getProductSize() {
		return productSize;
	}
	public void setProductSize(String productSize) {
		this.productSize = productSize;
	}
	public int getProductInvntCnt() {
		return productInvntCnt;
	}
	public void setProductInvntCnt(int productInvntCnt) {
		this.productInvntCnt = productInvntCnt;
	}
	public String getProductModel() {
		return productModel;
	}
	public void setProductModel(String productModel) {
		this.productModel = productModel;
	}
	public int getProductOptionNum() {
		return productOptionNum;
	}
	public void setProductOptionNum(int productOptionNum) {
		this.productOptionNum = productOptionNum;
	}
	public int getProductCode() {
		return productCode;
	}
	public void setProductCode(int productCode) {
		this.productCode = productCode;
	}
	
}
