package com.kirsh.www.biz.orderDetail;

public class OrderDetailVO {
	private int orderDetailNum;
	private int orderNum;
	private int productCode;
	private int productCnt;
	private String productSize;
	private int productCntPrice;
	private String productName;
	private String productImageUrl2;
	
	public int getOrderDetailNum() {
		return orderDetailNum;
	}
	public void setOrderDetailNum(int orderDetailNum) {
		this.orderDetailNum = orderDetailNum;
	}
	public int getOrderNum() {
		return orderNum;
	}
	public void setOrderNum(int orderNum) {
		this.orderNum = orderNum;
	}
	public int getProductCode() {
		return productCode;
	}
	public void setProductCode(int productCode) {
		this.productCode = productCode;
	}
	public int getProductCnt() {
		return productCnt;
	}
	public void setProductCnt(int productCnt) {
		this.productCnt = productCnt;
	}
	public String getProductSize() {
		return productSize;
	}
	public void setProductSize(String productSize) {
		this.productSize = productSize;
	}
	public int getProductCntPrice() {
		return productCntPrice;
	}
	public void setProductCntPrice(int productCntPrice) {
		this.productCntPrice = productCntPrice;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getProductImageUrl2() {
		return productImageUrl2;
	}
	public void setProductImageUrl2(String productImageUrl2) {
		this.productImageUrl2 = productImageUrl2;
	}

		
}
